# Funzioni

# Addizione
def add(num1, num2):
    risultato_add = num1 + num2
    return risultato_add

# Sottrazione
def sottr(num1, num2):
    risultato_sottr = num1 - num2
    return risultato_sottr

# Moltiplicazione
def mult(num1, num2):
    risultato_mult = num1 * num2
    return risultato_mult

# Divisione
def div(num1, num2):
    risultato_div = num1/num2
    return risultato_div

