# Tipi di variabili
var_1 = 1               # int
var_2 = 1.0             # float
var_3 = "Ciao"          # stringa
var_4 = True            # boolean
lst_voti = [6,7,6,8]    # lista

# Stampa tipi di variabili
print(type(var_1))
print(type(var_2))
print(type(var_3))
print(type(lst_voti))

# Metodi principali di una lista
print(lst_voti)
print(lst_voti[0])
print(len(lst_voti))
print(lst_voti[-1])
print(lst_voti[1:3])